package com.syntax.class01;

public class PrintOrPrintln {

	public static void main(String[] args) {
	
		// see difference between println vs print
		
		/*
		 * spaces matter inside "" 
		 * only spaces do not matter anywhere else
		 * since they will be treated as white spaces -ignored
		 */
		
		System.out.println("Hello");
		
		System.out.println("How are you");
		
		System.out.println("I am learning coding");

	}

}
